// Resolve issuer ticker -> website domain via the logo.dev SEARCH API (needs the
// secret key LOGODEV_SECRET). For each issuer we search its English company name,
// pick the best candidate (prefer .co.th, name/brand match, drop rep-offices &
// funds), then VERIFY the logo actually renders via img.logo.dev before keeping
// it. Writes src/data/issuerDomains.ts. Manual ISSUER_DOMAINS still win.
//
// Usage: npm run fetch:logos   (needs LOGODEV_SECRET + VITE_LOGODEV_TOKEN)
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const env = readFileSync(resolve(process.cwd(), ".env.local"), "utf8");
const secret = env.match(/^LOGODEV_SECRET=(.+)$/m)?.[1]?.trim();
const pub = env.match(/^VITE_LOGODEV_TOKEN=(.+)$/m)?.[1]?.trim();
if (!secret || !pub) {
  console.error("LOGODEV_SECRET and/or VITE_LOGODEV_TOKEN missing in .env.local");
  process.exit(1);
}

const catalog = JSON.parse(readFileSync(resolve(process.cwd(), "public/bond-catalog.json"), "utf8"));
const ticker = (s) => (s.match(/^[A-Za-z]+/)?.[0] ?? s).toUpperCase();

const nameByTicker = new Map();
for (const b of catalog.items) {
  const t = ticker(b.symbol);
  if (!nameByTicker.has(t) && b.nameEn) nameByTicker.set(t, b.nameEn);
}

// Pull the issuer's company name out of the (messy) bond title.
function extractCompany(nameEn) {
  const m = nameEn.match(
    /(?:DEBENTURES OF |CALLABLE DEBENTURES OF |PERPETUAL DEBENTURES OF |OF THE |OF )(.+?)(?: PUBLIC COMPANY LIMITED| PUBLIC CO| COMPANY LIMITED| PCL| PLC|['’]s | DUE | NO\.| NO | DEBENTURE)/i,
  );
  const raw = m ? m[1] : nameEn;
  return raw.replace(/\s+/g, " ").trim();
}

const BAD = /office|represent|relocation|\bfund\b|services|siding|holdings? corp|advisor/i;

function pickDomain(results, query) {
  const words = query.toLowerCase().replace(/[^a-z0-9 ]/g, " ").split(/\s+/).filter((w) => w.length >= 4);
  const scored = (results ?? [])
    .filter((r) => r.domain && !BAD.test(r.name ?? ""))
    .map((r) => {
      const d = r.domain.toLowerCase();
      let s = 0;
      if (d.endsWith(".co.th")) s += 4;
      else if (d.endsWith(".com") || d.endsWith(".th")) s += 1;
      if (words.some((w) => d.includes(w))) s += 3;
      s -= d.length * 0.02;
      return { d: r.domain, s };
    })
    .sort((a, b) => b.s - a.s);
  return scored.map((x) => x.d);
}

async function search(q) {
  try {
    const r = await fetch(`https://api.logo.dev/search?q=${encodeURIComponent(q)}`, {
      headers: { Authorization: `Bearer ${secret}` },
    });
    if (r.status !== 200) return [];
    return await r.json();
  } catch {
    return [];
  }
}

async function renders(domain) {
  try {
    const r = await fetch(`https://img.logo.dev/${domain}?token=${pub}&size=64&format=png&fallback=404`);
    return r.status === 200;
  } catch {
    return false;
  }
}

const found = {};
const entries = [...nameByTicker.entries()];
let i = 0;
for (const [t, nameEn] of entries) {
  i++;
  const q = extractCompany(nameEn);
  if (q.length >= 3 && !/^[\d.$]/.test(q)) {
    const cands = pickDomain(await search(q), q);
    for (const d of cands.slice(0, 4)) {
      if (await renders(d)) {
        found[t] = d;
        break;
      }
    }
  }
  if (i % 25 === 0) console.log(`…${i}/${entries.length}, resolved ${Object.keys(found).length}`);
  await new Promise((r) => setTimeout(r, 60)); // be gentle on the API
}

const keys = Object.keys(found).sort();
const out =
  `// Auto-discovered issuer ticker -> website domain via logo.dev search + image\n` +
  `// verification. Generated by scripts/fetch-issuer-logos.mjs — do not edit by\n` +
  `// hand; rerun \`npm run fetch:logos\`. Manual ISSUER_DOMAINS (src/lib/issuerLogo.ts)\n` +
  `// take precedence over these.\n\n` +
  `export const DISCOVERED_DOMAINS: Record<string, string> = {\n` +
  keys.map((k) => `  ${k}: ${JSON.stringify(found[k])},`).join("\n") +
  `\n};\n`;
writeFileSync(resolve(process.cwd(), "src/data/issuerDomains.ts"), out);
console.log(`done: ${keys.length}/${entries.length} resolved -> src/data/issuerDomains.ts`);
